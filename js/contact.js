const langSelector = document.getElementById("lang");
const elements = document.querySelectorAll("[data-i18n]");
const placeholders = {
  search: document.querySelector('input[placeholder][data-i18n-placeholder="search"]'),
  your_full_name: document.querySelector('input[placeholder][data-i18n-placeholder="your_full_name"]'),
  your_email_address: document.querySelector('input[placeholder][data-i18n-placeholder="your_email_address"]'),
  feel_free: document.querySelector('input[placeholder][data-i18n-placeholder="feel_free"]'), 
  nhap_address: document.querySelector('input[placeholder][data-i18n-placeholder="nhap_address"]'),

};

// ✅ Thay đổi ngôn ngữ
function changeLanguage(lang) {
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Cập nhật placeholder input
  if (placeholders.search && translations[lang].search) {
    placeholders.search.placeholder = translations[lang].search;
  }
  if (placeholders.your_full_name && translations[lang].your_full_name) {
    placeholders.your_full_name.placeholder = translations[lang].your_full_name;
  }
  if (placeholders.your_email_address && translations[lang].your_email_address) {
    placeholders.your_email_address.placeholder = translations[lang].your_email_address;
  }
  if (placeholders.feel_free && translations[lang].feel_free) {
    placeholders.feel_free.placeholder = translations[lang].feel_free;
  }
  if (placeholders.nhap_address && translations[lang].nhap_address) {
    placeholders.nhap_address.placeholder = translations[lang].nhap_address;
  }

  localStorage.setItem("language", lang);
}

// ✅ Khi người dùng thay đổi ngôn ngữ
langSelector.addEventListener("change", () => {
  const lang = langSelector.value;
  changeLanguage(lang);
});

